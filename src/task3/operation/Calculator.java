package task3.operation;

/**
 * Интерфейс калькулятора для выполнения базовых арифметических операций
 * над значениями типа {@code T}.
 *
 * @param <T> тип операндов и результата операций
 */
public interface Calculator<T> {

    /**
     * Складывает два значения.
     *
     * @param a первый операнд
     * @param b второй операнд
     * @return результат сложения
     */
    T add(T a, T b);

    /**
     * Вычитает второе значение из первого.
     *
     * @param a уменьшаемое
     * @param b вычитаемое
     * @return результат вычитания
     */
    T subtract(T a, T b);

    /**
     * Умножает два значения.
     *
     * @param a первый операнд
     * @param b второй операнд
     * @return результат умножения
     */
    T multiply(T a, T b);

    /**
     * Делит первое значение на второе.
     *
     * @param a делимое
     * @param b делитель
     * @return результат деления
     */
    T divide(T a, T b);
}
